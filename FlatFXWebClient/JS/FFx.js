function numberWithCommas(x) {
    x = x.toString();
    var pattern = /(-?\d+)(\d{3})/;
    while (pattern.test(x))
        x = x.replace(pattern, "$1,$2");
    return x;
}

function getCurrencySign(ISO) {
    if (ISO == 'ILS')
        return '₪';
    else if (ISO == 'EUR')
        return '€';
    else if (ISO == 'USD')
        return '$';
    else
        return 'Unknown';
}


function wait(ms) {
    var start = new Date().getTime();
    var end = start;
    while (end < start + ms) {
        end = new Date().getTime();
    }
}

